import math

def golden_ratio():
    """this function print golden ratio"""
    print((1+math.sqrt(5))/2)

def square_five():
    """this function that print 5^2"""
    print(math.pow(5,2))

def hypotenuse():
    """this function calculate the hypotenuse in right angled triangle"""
    print(math.sqrt(math.pow(4,2)+math.pow(5,2)))

def pi():
    """this function print the value of pi"""
    print(math.pi)

def e():
    """this function print the value of e"""
    print(math.e)

